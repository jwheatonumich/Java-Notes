//While loops will run while a specified condition is true
var ourArray = [];
var i = 0;
while(i < 5) {
  ourArray.push(i);
  i++;
}

//For loop specifies the number of times to run with inputs of initialization, test whether to keep going, and a final expression
var ourArray = [];
for (var i = 0; i < 5; i++) {
  ourArray.push(i);
}